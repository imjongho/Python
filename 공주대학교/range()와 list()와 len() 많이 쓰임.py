print('x =', list(range(1, 5)))
# range(start, stop) ----> (start) ~ (stop - 1) => 범위

print('---------------------------')

values = [1, 2, 3, 4, 5]
print('x =', len(values))
# len() 함수는 리스트 안에 있는 요소의 수를 반환한다.

print('---------------------------')

days = [1, 2, 3, 4, 5, 6, 7]
print("days =", days)
print("days[2] =", days[2])     # index가 0부터 시작되므로, 2는 3번쨰 요소를 의미
# 리스트를 생성하려면, 대괄호[] 안에 콤마(,)로 값을 구분해 넣으면 된다.
# 객체(object)처럼 리스트도 객체이므로 변수에 할당할 수 있다.
