# sort()는 리스트를 오름차순으로 정렬함
# reverse()는 리스트를 역순으로 정렬

letters = ['c', 'a', 'd', 'e', 'b']

letters.sort()                  # letters을 오름차순으로 정렬
print('letters =', letters)     # letters의 원본 리스트가 변경됨

# 현재 list값 'a', 'b', 'c', 'd', 'e'

letters.reverse()               # letters을 역순으로 정렬
print('letters =', letters)     # letters의 원본 리스트가 변경됨
