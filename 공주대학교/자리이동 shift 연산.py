print(0b00111100 >> 2)  # shift right >>은 '15' 즉 0b00001111을 출력
print(0b00111100 << 2)  # shift left <<은 '240' 즉 0b11110000을 출력

print(0b11101100)       # '236' 즉 0b00001111을 출력   
print(0b11101100 >> 2)  # '59' 즉 0b00111011을 출력
print('-------------------')
print(0b11101100 << 2)  # '944' 즉 0b1110110000을 출력 
print(bin(0b11101100 << 2))  # '944' 즉 0b1110110000을 출력 
